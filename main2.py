# for loops

for i in range(3):
    print("For loop: ", i)

print()

for i in range(3):
    print("For loop: ", i+1)

print()

for i in range(10, 20):  # first parameter is inclusive, second is exclusive
    print(i)

print()

for i in range(10, 20, 2):  # the 3rd parameter select how to count (2 numbers at a time in this example)
    print(i)

print()

for i in "Valerio Failah":  # you can iterate on strings
    print(i)

print()

